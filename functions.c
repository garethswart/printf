/**
 * write_number - adds an int to a buffer
 * @is_positive: is 1 if 
 * @i: int to be converted.
 * 
 * Return: size.
 */
int write_number(int is_negative, int i)
{
	int t = 10000000;
	int digs = 0;
	int flg = 0;
	char *p[BUFF_SIZE];

	if (is_negative == 1)
	{
		p[digs] = 45;
		digs++;
	}
	while (t > 0)
	{
		if ((i / t) % 10 > 0 || flg == 1)
		{
			p[digs] = ((i / t) % 10) + 48;
			digs++;
			flg = 1;
		}
		t = t / 10;
	}
	write(1, p, digs);
	return (1);
}
